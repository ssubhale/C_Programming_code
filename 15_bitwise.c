#include <stdio.h>


int main(void)
{
    printf("bitwise 28 & 14 = %d\n", 28 & 14);
    printf("bitwise 28 | 14 = %d\n", 28 | 14);
    printf("bitwise 28 ^ 14 = %d\n", 28 ^ 14);

    printf("bitwise ~ 28 = %d\n", ~28);

    printf("bitwise 10 << 2 = %d\n", 10 << 2);
    printf("bitwise 10 >> 2 = %d\n", 10 >> 2);

    printf("bitwise 20 << 2 = %d\n", 20 << 2);
    printf("bitwise 20 >> 2 = %d\n", 20 >> 2);

    return 0;
}

